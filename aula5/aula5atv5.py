# O que são estruturas de repetição?
# As estruturas de repetição são úteis, por exemplo, para repetir uma série de operações semelhantes que são executadas para todos os elementos de uma lista ou de uma tabela de dados, ou simplesmente para repetir um mesmo processamento até que uma certa condição seja satisfeita.


# As estruturas de repetição são úteis, por exemplo, para repetir uma série de operações semelhantes que são executadas para todos os elementos de uma lista ou de uma tabela de dados, ou simplesmente para repetir um mesmo processamento até que uma certa condição seja satisfeita.


# Interação Estas estruturas são utilizadas quando não temos previamente a quantidade de vezes que um determinado trecho de código irá se repetir. É dito interativo porque necessitada da ação do usuário para que o mesmo pare de se repetir.


# As estruturas de repetição são basicamente três: enquanto-faça, faça-enquanto e para-faça.

