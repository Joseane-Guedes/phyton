# Lista
# São valores ordenados e mutáveis, que podem ser repetidos e estão contidos em dentro de um par de []
frutas = ['Maracuja', 'Uva', 'Morango']
print(30*'' + "Lista" + ''*30)
print(frutas)

# Tupla
# São valores ordenados e imutáveis,  podem ser repetidos e estão contidos em dentro de um par de ():
automoveis = ('Carro', 'Moto', 'Aviao')
print(30*'' + "Tupla" + ''*30)
print(automoveis)

# Dicionario
# São valores ordenados, mutáveis que não podem ser repetidos. Possuem uma estrutura de chave:valor, e estão contidos dentro de {}
pessoa = {
    "nome": "Renato",
    "idade": 45,
    "cidade": "Salvador"
}
print(30*'' + "Dicionário" + ''*30)
print(pessoa)

# Set
# Sets ou conjuntos, são valores desordenados, mutáveis e que podem ser repetidos. Esses valores são contidos dentro de {}, mas, diferente dos dicionários, não possuem a estrutura chave:valor.
cores = {'azul', 'amarelo', 'rosa'}
print(30*'' + "Sets" + ''*30)
print(cores)
